    private void limpiarCampos(){
        txtNombre.setText("");
        txtEdad.setText("");
        txtEmail.setText("");
        txtContraseña.setText("");
    }

    private void btnIniciarActionPerformed() {
        Login cambio = new Login();
        cambio.setVisible(true);
        this.dispose();
    }

    private void btnRegistrarmeActionPerformed() {
        try{
            String nombre = txtNombre.getText();
            Integer edad = Integer.parseInt(txtEdad.getText());
            String email = txtEmail.getText();
            String contraseña = new String(txtContraseña.getPassword());

            if ((nombre.isEmpty() || nombre.isBlank()) || (email.isEmpty() || email.isBlank())) {
                JOptionPane.showMessageDialog(this, "Por favor complete todos los campos", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            Usuario usuario = new Usuario();
            usuario.setNombre(nombre);
            usuario.setEdad(edad);
            usuario.setCorreo(email);
            usuario.setContrasena(contraseña);

            controlador.createUser(usuario);
            JOptionPane.showMessageDialog(null, "Usuario registrado correctamente");

            limpiarCampos();

        }catch(BackendException e){
            JOptionPane.showMessageDialog(null, "Error al registrar el usuario: " + e.getMessage());
        } catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Por favor ingrese valores numéricos válidos","Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al registrar el usuario: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE, null);
        }
    }

